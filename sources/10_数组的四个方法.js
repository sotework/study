// 数组的四个方法
let arr = [0, 1, 2, 3, 4, 5, 6, 7]
arr.push(arr.length) // 从末尾添加元素
console.log('push', arr);
arr = [0, 1, 2, 3, 4, 5, 6, 7]
arr.pop() // 从末尾删除一个元素
console.log('pop', arr);
arr = [0, 1, 2, 3, 4, 5, 6, 7]
arr.shift() // 从开始删除一个元素
console.log('shift', arr);
arr = [0, 1, 2, 3, 4, 5, 6, 7]
arr.unshift(11) // 从开始添加元素
console.log('unshift', arr);
